public class point {
    //instance variables
    private int x;
    private int y;

    //constructors
    public Point(int xcoord, int ycoord){
        x = xcoord;
        y = ycoord;
    }
    public Point(int coord){
        x = coord;
        y = coord;
    }
    public Point(){
        x = 0;
        y = 0;
    }
}
